<h2 class='info-h2'>使用說明</h2>
<h3>收藏景點</h3>
<ol>
	<li>點選「景點一覽」，顯示景點。</li>
	<li>點選景點列，可查看景點地址與開放時間。</li>
	<li>點選景點列右方之「<span class="glyphicon glyphicon-plus"></span>」鈕，將指定景點加入收藏列表。再次點擊可取消收藏。</li>	
</ol>
<h3>路線試算</h3>
<ol>
	<li>點選「路線試算」，顯示已收藏之景點。</li>
	<li>點選指定景點列，再點選「From」鈕，將其設為起點。</li>
	<li>點選指定景點列，再點選「To」鈕，將其設為終點。</li>
	<li>點選「<span class="glyphicon glyphicon-search"></span>」鈕，查詢路線。</li>
</ol>

